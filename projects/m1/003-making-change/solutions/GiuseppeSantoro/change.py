cents = 5
Nickel = cents
print("One Nickel is:",Nickel,"cents")

Dime = Nickel*2
print("One Dime is:",Dime,"cents")

Quarter = (Dime*2)+Nickel
print("One Quarter is:",Quarter,"cents")

Loonie = Quarter*4
print("One Loonei is:",Loonie,"cents")

#Loonie = 1 dollar
dollar = int(Loonie/100)
print("One Dollar is:",dollar,"Loonie")

Toonei = dollar*2
print("One Toonei is:",Toonei,"dollars")
