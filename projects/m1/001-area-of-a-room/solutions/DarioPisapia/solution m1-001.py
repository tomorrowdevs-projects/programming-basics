#Area of a room

whidth = float(input("Digit the width of your room in meters:\n"))
length = float(input("Digit the lenght of your room in meters:\n"))

room_area = whidth * length

print(f"\nYour room's whidth is: {whidth: .2f}m")
print(f"Your room's lenght is: {length: .2f}m")
print(f"Area of your room is: {room_area: .2f}mq")
