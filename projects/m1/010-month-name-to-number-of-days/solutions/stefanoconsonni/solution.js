const chosenMonth = prompt("Please enter the name of a month:").toLowerCase();

if (chosenMonth == "january") {
	alert("January has 31 days");
} else if (chosenMonth == "february") {
	alert("February has 28 or 29 days");
} else if (chosenMonth == "march") {
	alert("March has 31 days");
} else if (chosenMonth == "april") {
	alert("April has 30 days");
} else if (chosenMonth == "may") {
	alert("May has 31 days");
} else if (chosenMonth == "june") {
	alert("June has 30 days");
} else if (chosenMonth == "july") {
	alert("July has 31 days");
} else if (chosenMonth == "august") {
	alert("August has 31 days");
} else if (chosenMonth == "september") {
	alert("September has 30 days");
} else if (chosenMonth == "october") {
	alert("October has 31 days");
} else if (chosenMonth == "november") {
	alert("November has 30 days");
} else if (chosenMonth == "december") {
	alert("December has 31 days");
} else {
	alert("Please reload the page and enter the name of a month");
}
