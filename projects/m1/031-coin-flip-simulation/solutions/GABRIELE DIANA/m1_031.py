import random


def check(seq):
    if seq[-1] == seq[-2] == seq[-3]:
        return True


def total_set():
    turn = 0
    tries = []
    while turn != 10:
        coin = ("H", "T")
        count = 3
        flips = random.choices(coin, k=3)
        while True:
            if not check(flips):
                flips.append(random.choice(coin))
                count += 1
            else:
                output = " ".join(flips)
                print(output + " ({} flips)".format(count))
                break
        tries.append(count)
        turn += 1
    print("On average, {} flips were needed".format(sum(tries)/len(tries)))



def main():
    total_set()


if __name__ == '__main__':
    main()
